    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        mCategoria dts = new mCategoria();
        nCategoria func = new nCategoria();
        
        if(txtCategoria.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresa una categoria");
            txtCategoria.requestFocus();
            return;
        }
        
        dts.setnCategoria(txtCategoria.getText());
        
        if(func.insertar(dts)){
            JOptionPane.showConfirmDialog(rootPane, "Categoria registrada con Ã©xito");
            txtCategoria.setText("");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

