    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        if(txtIdEmpleado.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Ingrese Nombre del Empleado");
            txtIdEmpleado.requestFocus();
            return;
        }
        if(txtUsuario.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Ingrese un Usuario");
            txtUsuario.requestFocus();
            return;
        }
        if(txtPassword.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Ingrese una Contraseña");
            txtPassword.requestFocus();
            return;
        }
        
        mUsuario dts = new mUsuario();
        nUsuario func = new nUsuario();
        
        dts.setIdEmpleado(txtIdEmpleado.getText());
        dts.setUsuario(txtUsuario.getText());
        dts.setPassword(txtPassword.getText());
        int selecionado = cboAcceso.getSelectedIndex();
        dts.setAcceso((String) cboAcceso.getItemAt(selecionado));
        selecionado= cboEstado.getSelectedIndex();
        dts.setEstado((String) cboEstado.getItemAt(selecionado));
        
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                
                JOptionPane.showConfirmDialog(rootPane, "Registrado satisfctoriamente");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            dts.setIdUsuario(Integer.parseInt(txtIdUsuario.getText()));
            if(func.editar(dts)){
                
                JOptionPane.showConfirmDialog(rootPane, "modificación satisfctoria");
                mostrar("");
                limpiar();
                inhabilitar();
            }
            
        }
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       
        mUsuario dts = new mUsuario();
        nUsuario func = new nUsuario();
        
        if(!txtIdUsuario.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Desea eliminar?", "Confimar",2);
            if(confirmacion==0){
                
                dts.setIdUsuario(Integer.parseInt(txtIdUsuario.getText()));
                func.eliminar(dts);
                mostrar("");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

