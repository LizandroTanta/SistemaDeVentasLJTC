       txtIdUsuario.setText(tablaListado.getValueAt(fila, 0).toString());
       txtIdEmpleado.setText(tablaListado.getValueAt(fila, 1).toString());
       txtApellido.setText(tablaListado.getValueAt(fila, 2).toString());
